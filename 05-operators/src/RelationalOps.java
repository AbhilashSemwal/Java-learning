public class RelationalOps{

	public static void main(String[] args){
		
		operation();
	}

	public static void operation(){
		int a = 5;
		int b = 7;

		System.out.println("a>b: " + (a>b));
		System.out.println("a<b: " + (a<b));
		System.out.println("a==b: " + (a==b));
		System.out.println("a != b: " + (a!=b));
		System.out.println("a>=b: " + (a>=b));
		System.out.println("a<=b: " + (a<=b));
	}
}
